export function getDictionaryValue(dict: TranslationDict, key: string): string | undefined {
    return key.split('.').reduce<string | TranslationDict | undefined>((acc, part) => {
        if (!acc || typeof acc === 'string') return acc;
        return acc[part];
    }, dict) as string | undefined;
}

export function getDictionary(lang: SupportedLanguage): TranslationDict {
    return dictionaries[lang];
}

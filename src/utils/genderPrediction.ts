export function predictGender(name: string): Gender {
    if (MALE_NAMES.has(name)) return 'male';
    if (FEMALE_NAMES.has(name)) return 'female';

    // Heuristic Check
    for (const suffix of FEMALE_SUFFIXES) {
        if (name.endsWith(suffix)) return 'female';
    }

    for (const suffix of MALE_SUFFIXES) {
        if (name.endsWith(suffix)) return 'male';
    }

    return 'neutral';
}
